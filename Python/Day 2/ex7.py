dates = input('Enter dates: ')
splitdates = dates.split(' ')
date1 = splitdates[0].split('/')
d1 = int(date1[0])
m1 = int(date1[1])
y1 = int(date1[2])
date2 = splitdates[1].split('/')
d2 = int(date2[0])
m2 = int(date2[1])
y2 = int(date2[2])
c1 = 365*y1 + y1//4 - y1//100 + y1//400 + (306*m1 + 5)//10 + (d1 - 1)
c2 = 365*y2 + y2//4 - y2//100 + y2//400 + (306*m2 + 5)//10 + (d2 - 1)
days_passed = abs(c1 - c2)
print (int(days_passed), 'days have elapsed between the two dates.')
